package es.uah.mat.sigueme.estadistica;

public class Rango {

	private Integer fin;
	private Integer inicio;

	public Rango(Integer inicio, Integer fin) {
		this.inicio = inicio;
		this.fin = fin;
	}

	public Integer getFin() {
		return fin;
	}

	public Integer getInicio() {
		return inicio;
	}

}
